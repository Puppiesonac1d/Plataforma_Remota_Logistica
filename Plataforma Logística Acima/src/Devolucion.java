    private void tblSalidasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSalidasMouseClicked
        /*
        
        select codigoBulto as 'Número de Bulto',largo as 'Largo',alto as 'Alto', 
        ancho as 'Ancho',peso as 'Peso', direccionDespacho as 'Dirección de Despacho', guiaDespacho as 'Guía de Despacho', 
        chofer as 'Chofer' from bulto where idSalida = ?;
         */
        try {
            int index = tblSalidas.getSelectedRow();
            String query = "SELECT \n"
                    + "    idSalida AS 'N° de salida',\n"
                    + "    idOrden AS 'Nota de Venta',\n"
                    + "    codigoBulto AS 'Número de Bulto',\n"
                    + "    largo AS 'Largo',\n"
                    + "    alto AS 'Alto',\n"
                    + "    ancho AS 'Ancho',\n"
                    + "    peso AS 'Peso',\n"
                    + "    direccionDespacho AS 'Dirección de Despacho',\n"
                    + "    guiaDespacho AS 'Guía de Despacho',\n"
                    + "    chofer AS 'Chofer'\n"
                    + "FROM\n"
                    + "    bulto\n"
                    + "WHERE\n"
                    + "    idSalida = ?";
            PreparedStatement pst;
            pst = cn.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(tblSalidas.getValueAt(index, 0).toString()));
            ResultSet rs = pst.executeQuery();
            tblBultos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(Devolucion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblSalidasMouseClicked

    private void tblBultosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBultosMouseClicked
        try {
            int index = tblBultos.getSelectedRow();
            String query = "select codigoBulto as 'N° de Bulto',idProducto as 'ID de Producto',nombreProducto as 'Nombre de Producto',stockRestado as 'Cantidad en Bulto'\n"
                    + "from detalleSalida where idSalida = ? and idOrden = ? and codigoBulto = ?;";
            PreparedStatement pst;
            pst = cn.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(tblBultos.getValueAt(index, 0).toString()));
            pst.setInt(2, Integer.parseInt(tblBultos.getValueAt(index, 1).toString()));
            pst.setString(3, tblBultos.getValueAt(index, 2).toString());
            ResultSet rs = pst.executeQuery();
            tblProductosBultos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(Devolucion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblBultosMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String respuesta = JOptionPane.showInputDialog("Ingrese la cantidad a devolver a bodega");
            int index = tblProductosBultos.getSelectedRow();
            int cantidadTabla = Integer.parseInt(tblProductosBultos.getValueAt(index, 3).toString());
            cantidadTabla = cantidadTabla - Integer.parseInt(respuesta);
            tblProductosBultos.setValueAt(cantidadTabla, index, 3);
            //update inventario set stock = stock + 1 where idProducto = 1;
            String query = "update inventario set stock = stock + ? where idProducto = ?";
            PreparedStatement pst;
            pst = cn.prepareStatement(query);
            pst.setInt(1, cantidadTabla);
            pst.setInt(2, Integer.parseInt(tblProductosBultos.getValueAt(index, 1).toString()));
            pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Devolucion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

