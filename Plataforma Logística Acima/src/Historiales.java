    private void btnHistorialSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHistorialSalidaActionPerformed
        try {
            //this.dispose();
            HistorialSalida histSalida = new HistorialSalida();
            histSalida.setVisible(true);
            String query = "Select s.idSalida as 'ID de Salida',s.idOrden as 'ID de Orden', s.codigoOrdenCompra as 'Código de Orden de Compra',\n"
                    + "tipoTransporte as 'Tipo de Transporte', b.nombreBodega as 'Nombre de Bodega',s.seccion as 'Sección', bu.codigoBulto as 'Codigo de Bulto',\n"
                    + "fechaSalida as 'Fecha de Salida',\n"
                    + "s.ordenTransporte as 'Orden de Transporte' from salida s join bodega b on s.idBodega = b.idBodega join bulto bu on bu.codigoOrdenCompra = s.codigoOrdenCompra\n"
                    + "GROUP BY s.idOrden;";
            PreparedStatement pst = cn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            histSalida.tblHistorialSalida.setModel(DbUtils.resultSetToTableModel(rs));
            histSalida.setTitle("Historial de Salida");
            // JOptionPane.showMessageDialog(null, "LISTO");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnHistorialSalidaActionPerformed

    private void btnHistorialIngreso1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHistorialIngreso1ActionPerformed
        try {
            //this.dispose();
            HistorialIngresos histIngreso = new HistorialIngresos();

            histIngreso.setVisible(true);
            String query = "SELECT \n"
                    + "    ing.numeroCotizacion AS 'N° de Cotización',\n"
                    + "    ing.notaVenta AS 'Nota de Venta',\n"
                    + "    ing.tipoTransporte AS 'Transporte',\n"
                    + "    ing.idProducto AS 'ID de Producto',\n"
                    + "    inv.NOMBREPRODUCTO AS 'Nombre de Producto',\n"
                    + "    ing.guiaDespachoProveedor AS 'Guía de Despacho',\n"
                    + "    ing.codigoGeneradoQR AS 'Código QR',\n"
                    + "    ing.stockIngresado AS 'Stock',\n"
                    + "    ing.fechaIngreso AS 'Fecha de Ingreso'\n"
                    + "FROM\n"
                    + "    ingreso ing\n"
                    + "        JOIN\n"
                    + "    inventario inv ON inv.idProducto = ing.idProducto;";
            PreparedStatement pst = cn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            histIngreso.tblHistorialIngreso.setModel(DbUtils.resultSetToTableModel(rs));
            histIngreso.setTitle("Historial de Ingresos");
            // JOptionPane.showMessageDialog(null, "LISTO");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnHistorialIngreso1ActionPerformed

