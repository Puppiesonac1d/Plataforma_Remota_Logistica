    private void cmbRegionMercaderiaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbRegionMercaderiaItemStateChanged
        String region = cmbRegionMercaderia.getSelectedItem().toString();

        if (region.equals("Seleccione Región")) {
            cmbComunaMercaderia.disable();
        } else {
            cmbComunaMercaderia.removeAllItems();
            cmbComunaMercaderia.enable();

            try {
                String query = "select co.nombreComuna "
                        + "from Comuna co join Region re on co.IDRegion = re.IDRegion "
                        + "where re.NombreRegion = ?  "
                        + "order by co.nombreComuna asc";
                String param = region;
                PreparedStatement pst = cn.prepareStatement(query);
                pst.setString(1, param);
                ResultSet rs = pst.executeQuery();
                cmbComunaMercaderia.addItem("Todas las Comunas");
                while (rs.next()) {
                    cmbComunaMercaderia.addItem(rs.getString(1));
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_cmbRegionMercaderiaItemStateChanged

