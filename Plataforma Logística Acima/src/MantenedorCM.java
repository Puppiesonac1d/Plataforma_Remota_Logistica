    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            String query = "UPDATE convenioMarco SET nombreConvenio ='" + txtNombreConvenio.getText() + "' WHERE codigoConvenio = ?";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, txtCodigoConvenio.getText());
            int up = pst.executeUpdate();
            System.out.println(String.format("Row affected %d", up));
            jTextField1.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        try {
            String query = "Delete from convenioMarco where codigoConvenio=(?)";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, txtCodigoConvenio.getText());
            int up = pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) tblConvenios.getModel();
            model.setRowCount(0);
            System.out.println(String.format("Row affected %d", up));
            txtCodigoConvenioBorrar.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try {
            String query = "INSERT INTO convenioMarco (CodigoConvenio,NombreConvenio) VALUES (?,?)";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, txtCodigoConvenio.getText());
            pst.setString(2, txtNombreConvenio.getText());
            int up = pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) tblConvenios.getModel();
            model.setRowCount(0);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

